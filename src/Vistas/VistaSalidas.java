    public void InicializarTblInventario() {
        tblSalidas.setDefaultRenderer(Object.class, new TablaRender());
        
        modeloTblSalida = new DefaultTableModel(EncabezadoTblSalida, 0) {
            Class[] types = new Class[]{
                java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,
                java.lang.Object.class,java.lang.Object.class,java.lang.Object.class,java.lang.Object.class
            };

            public Class getColumnClass(int col) {
                return types[col];
            }

            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };

        tblSalidas.setModel(modeloTblSalida);

        tblSalidas.getColumnModel().getColumn(0).setPreferredWidth(30);
        tblSalidas.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblSalidas.getColumnModel().getColumn(2).setPreferredWidth(350);
        tblSalidas.getColumnModel().getColumn(3).setPreferredWidth(80);
        tblSalidas.getColumnModel().getColumn(4).setPreferredWidth(80);
        tblSalidas.getColumnModel().getColumn(5).setPreferredWidth(80);
        tblSalidas.getColumnModel().getColumn(6).setPreferredWidth(200);
        tblSalidas.getColumnModel().getColumn(6).setPreferredWidth(110);
        
        tblSalidas.getTableHeader().setReorderingAllowed(false);

        for (int i = 0; i < modeloTblSalida.getColumnCount(); i++) {
            tblSalidas.getColumnModel().getColumn(i).setResizable(false);
            DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
            tcr.setFont(new Font("Tahoma", 0, 12));
            
//            if(i == 3 ){
//                tcr.setHorizontalAlignment(SwingConstants.RIGHT);
//               
//            }else{
                tcr.setHorizontalAlignment(SwingConstants.CENTER);
                
//            }
            tcr.setForeground(new Color(22, 108, 151));
            tblSalidas.getColumnModel().getColumn(i).setCellRenderer(tcr);
            
        }
        JTableHeader header = tblSalidas.getTableHeader();

        ((DefaultTableCellRenderer) header.getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);
        ((DefaultTableCellRenderer) header.getDefaultRenderer()).setPreferredSize(new Dimension(0, 35));
        ((DefaultTableCellRenderer) header.getDefaultRenderer()).setVerticalAlignment(JLabel.CENTER);
        
    }
